// Generates tests/visual/clip-heights.generated.ts from the committed baseline PNGs.
//
// WHY: the oplist/bonus top-3 region has a systemic ±1px sub-pixel HEIGHT jitter across all
// browsers (chromium-desktop/android, webkit-desktop/ios). Playwright hard-fails on ANY
// dimension difference, so element-bbox screenshots flake. Fix: screenshot a page `clip` whose
// x/y/width come from the live boundingBox() but whose HEIGHT is a FIXED per-(geo,project)
// constant — dimensions become constant by construction and the jitter can never mismatch.
//
// The heights are read from the actual rendered baselines (per geo × project) rather than
// hand-typed, so this stays correct if geos are added/removed — just re-run:
//     node tests/visual/generate-clip-heights.mjs
//
// Run this AFTER a baseline set exists for the current layout, and re-run whenever the geo list
// or card layout changes. Skipped-test combos (e.g. us, root@webkit-ios) may carry stale heights
// from an older capture; harmless because those tests never execute (and never read the value).

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import path from 'node:path';

/** Read width/height from a PNG's IHDR chunk (offsets 16/20, big-endian uint32) — no image lib. */
function pngHeight(file) {
  const b = readFileSync(file);
  if (b.length < 24 || b.toString('ascii', 12, 16) !== 'IHDR') {
    throw new Error(`Not a PNG (no IHDR): ${file}`);
  }
  return b.readUInt32BE(20);
}

const SPECS = [
  { name: 'oplist', dir: 'tests/visual/oplist.spec.ts-snapshots', prefix: 'oplist-' },
  { name: 'bonus', dir: 'tests/visual/bonus-pages.spec.ts-snapshots', prefix: 'bonus-' },
];

const result = {};
let total = 0;
for (const spec of SPECS) {
  const map = {};
  const files = readdirSync(spec.dir)
    .filter((f) => f.startsWith(spec.prefix) && f.endsWith('-linux.png'))
    .sort();
  for (const f of files) {
    // Filename shape: <prefix><geo>-visual-<project>-linux.png  (geo may contain hyphens, e.g. be-fr)
    const base = f.slice(spec.prefix.length, -'-linux.png'.length); // <geo>-visual-<project>
    const sep = base.indexOf('-visual-');
    if (sep === -1) continue;
    const geo = base.slice(0, sep);
    const project = base.slice(sep + '-visual-'.length); // e.g. chromium-desktop
    map[`${geo}|visual-${project}`] = pngHeight(path.join(spec.dir, f));
    total++;
  }
  // Stable key order for a deterministic, review-friendly diff.
  result[spec.name] = Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b)));
}

const out = `// AUTO-GENERATED by tests/visual/generate-clip-heights.mjs — DO NOT EDIT BY HAND.
// Fixed clip HEIGHT (px) per "geo|project", read from the committed baseline captures. Used so the
// oplist/bonus screenshots clip to a constant height and the ±1px sub-pixel jitter can never
// produce a dimension mismatch. Regenerate: node tests/visual/generate-clip-heights.mjs
/* eslint-disable */
export const CLIP_HEIGHTS: Record<'oplist' | 'bonus', Record<string, number>> = ${JSON.stringify(result, null, 2)};
`;

writeFileSync('tests/visual/clip-heights.generated.ts', out);
console.log(`wrote tests/visual/clip-heights.generated.ts — ${total} entries (oplist: ${Object.keys(result.oplist).length}, bonus: ${Object.keys(result.bonus).length})`);
